    private void bequalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bequalActionPerformed
       try {
            // Split the expression by operators to handle multiple operations
            String[] numbers = expression.split("[-+*/%]");
            String[] operators = expression.split("[0-9.]+");
            
            // First handle multiplication and division (higher precedence)
            java.util.List<Double> nums = new java.util.ArrayList<>();
            java.util.List<String> ops = new java.util.ArrayList<>();
            
            for (String num : numbers) {
                if (!num.isEmpty()) {
                    nums.add(Double.parseDouble(num));
                }
            }
            
            for (String op : operators) {
                if (!op.isEmpty()) {
                    ops.add(op);
                }
            }

            // Process multiplication and division first
            for (int i = 0; i < ops.size(); ) {
                if (ops.get(i).equals("*") || ops.get(i).equals("/") || ops.get(i).equals("%")) {
                    double result = calculate(nums.get(i), nums.get(i + 1), ops.get(i));
                    nums.set(i, result);
                    nums.remove(i + 1);
                    ops.remove(i);
                } else {
                    i++;
                }
            }

            // Then process addition and subtraction
            double result = nums.get(0);
            for (int i = 0; i < ops.size(); i++) {
                result = calculate(result, nums.get(i + 1), ops.get(i));
            }

            t1.setText(String.valueOf(result));
            expression = String.valueOf(result);
            operator = "";
        } catch (Exception e) {
            t1.setText("Error");
            expression = "";
        
    }// TODO add your handling code here:
    }//GEN-LAST:event_bequalActionPerformed

