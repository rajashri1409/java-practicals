    /**
     * @param args the command line arguments
     */
    private void calculateIntermediate() {
        try {
            String[] parts = expression.split("[-+*/%]");
            if (parts.length >= 2) {
                num1 = Double.parseDouble(parts[0]);
                num2 = Double.parseDouble(parts[1]);
                double result = calculate(num1, num2, operator);
                expression = String.valueOf(result);
                num1 = result;
            }
        } catch (Exception e) {
            t1.setText("Error");
            expression = "";
        }
    }  
    private double calculate(double n1, double n2, String op) {
        switch (op) {
            case "+": return n1 + n2;
            case "-": return n1 - n2;
            case "*": return n1 * n2;
            case "/": return n2 != 0 ? n1 / n2 : Double.NaN;
            case "%": return n1 % n2;
            default: return 0;
        }
    }
    
    
    
    
    
    
